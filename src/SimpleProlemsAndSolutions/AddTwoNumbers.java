package SimpleProlemsAndSolutions;

public class AddTwoNumbers {
    private static int sumTwoNumber(int n, int m) {
        return n+m;
    }
    public static void main(String[] args){
        var n = 10;
        var m = 20;
        var result = sumTwoNumber(n,m);
        System.out.println(result);
    }
}
